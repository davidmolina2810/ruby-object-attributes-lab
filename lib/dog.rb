class Dog 
  # setter method for dog name 
  def name=(new_name)
    @name = new_name
  end

  # getter method for dog name
  def name
    @name
  end 

  # setter method for dog breed
  def breed=(new_breed)
    @breed = new_breed
  end
  
  # getter method for dog breed
  def breed
    @breed
  end
end
